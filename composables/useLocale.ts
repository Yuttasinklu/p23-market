const interpolate = (text: string, params?: Record<string, string | number>) => {
  if (!params) return text;
  return text.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ""));
};

export const useLocale = () => {
  const cookie = useCookie<Locale>("mmarket-locale", { default: () => "en" });
  const locale = useState<Locale>("locale", () => cookie.value || "en");

  const setLocale = (value: Locale) => {
    locale.value = value;
    cookie.value = value;
  };

  const t = (key: string, params?: Record<string, string | number>) => {
    const selected = messages[locale.value][key] ?? messages.en[key] ?? key;
    return interpolate(selected, params);
  };

  return { locale, setLocale, t };
};
